import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import warnings
warnings.filterwarnings('ignore')

df=pd.read_csv('/content/traffic[1].csv')
df.head()

"""preprocessing dataset

"""

df.isnull().sum()

df.info()

df['DateTime']=pd.to_datetime(df['DateTime'],format='%Y-%m-%d %H:%M:%S')

df.info()

df=df.drop('Count',axis=1)

df.head()

plt.figure(figsize=(10,5))
plt.plot(df['DateTime'],df['Vehicles'])
plt.xlabel('DateTime')

df=df.drop(['ID'],axis=1)

df.head()

df['y'].value_counts()

df['ds']=df.index

df.head()

"""##Input Split"""

size=60
from sklearn.model_selection import train_test_split
train,test=train_test_split(df,test_size=size,shuffle=False)

train.head()

"""Model Training

"""

from fbprophet import prophet
model=prophet(yearly_seasonality=True,seasonality_prior_scale=0.1)
model.fit(train)

future=model.make_future_dataframe(periods=size,freq='H')

future

forecast=model.predict(future)
forecast.head()

model.plot_components(forecast)

pred=forecast.iloc[-60:,:]
len(pred)

"""Test results"""

plt.figure(figsize=(10,5))
plt.plot(test['ds'],test['y'])
plt.plot(pred['ds'],pred['yhat'],color='red')
plt.show()

